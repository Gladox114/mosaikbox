import logging
import time

logger = logging.getLogger(__name__)


def timeit_decorator(func):
    def wrapper(*args, **kwargs):
        start_time = time.time()
        result = func(*args, **kwargs)
        end_time = time.time()
        logger.debug(f"Function {func.__name__} took {end_time - start_time} seconds to run.")

        return result

    return wrapper
